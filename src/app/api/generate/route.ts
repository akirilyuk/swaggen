const MIDDLEWARE_TEMPLATE = `import { NextRequest, NextResponse } from 'next/server';

export interface PipelineContext {
  requestId?: string;
  requestStart?: number;
  userId?: string;
  token?: string;
  validatedBody?: unknown;
  corsHeaders?: Record<string, string>;
  custom: Record<string, unknown>;
}

export interface SwaggenMiddlewareResult {
  response?: NextResponse;
  ctx?: Partial<Omit<PipelineContext, 'custom'>> & {
    custom?: Record<string, unknown>;
  };
}

export type SwaggenMiddleware = (
  req: NextRequest,
  ctx: PipelineContext,
) => Promise<SwaggenMiddlewareResult>;

export function createPipelineContext(): PipelineContext {
  return { custom: {} };
}

export function mergeContext(
  ctx: PipelineContext,
  partial?: SwaggenMiddlewareResult['ctx'],
): void {
  if (!partial) return;
  const { custom, ...rest } = partial;
  Object.assign(ctx, rest);
  if (custom) Object.assign(ctx.custom, custom);
}

export async function withMiddlewares(
  req: NextRequest,
  middlewares: SwaggenMiddleware[],
  handler: (req: NextRequest, ctx: PipelineContext) => Promise<NextResponse>,
): Promise<NextResponse> {
  const ctx = createPipelineContext();

  try {
    for (const mw of middlewares) {
      const result = await mw(req, ctx);
      if (result.response) return result.response;
      mergeContext(ctx, result.ctx);
    }
    return await handler(req, ctx);
  } catch (err) {
    const requestId = ctx.requestId ?? 'unknown';
    const message = err instanceof Error ? err.message : 'Internal server error';
    console.error(\`[error] \${req.method} \${req.nextUrl.pathname} [\${requestId}]\`, err);
    return NextResponse.json(
      { error: 'Internal server error', requestId, message },
      { status: 500 },
    );
  }
}
`;

