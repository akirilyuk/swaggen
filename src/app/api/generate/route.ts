/**
 * POST /api/generate
 *
 * Accepts an OpenAPI spec + project metadata and returns a ZIP archive
 * containing a complete Next.js project scaffold with the generated
 * route handlers, interfaces, Zod schemas, middlewares and services
 * overlaid on top inside `src/`.
 *
 * This runs entirely server-side so we can use Node fs/path safely.
 */
