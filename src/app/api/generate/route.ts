/** Recursively read all files under `dir`, returning relative paths + content. */
function readDirRecursive(dir: string, base: string = dir): GeneratedFile[] {
  const files: GeneratedFile[] = [];
  if (!fs.existsSync(dir)) return files;

  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...readDirRecursive(full, base));
    } else {
      files.push({
        path: path.relative(base, full),
        content: fs.readFileSync(full, 'utf-8'),
      });
    }
  }
  return files;
}

/** Remove a directory and all its contents. */
function cleanup(dir: string) {
  if (fs.existsSync(dir)) {
    fs.rmSync(dir, { recursive: true, force: true });
  }
}

/**
 * Collect all generated files from `projectDir` into a ZIP archive and
 * resolve with the Buffer. The archive preserves the full folder structure
 * so the download extracts as a ready-to-use Next.js project.
 */
function buildZipBuffer(projectDir: string): Promise<Buffer> {
  return new Promise((resolve, reject) => {
    const chunks: Buffer[] = [];
    const passThrough = new PassThrough();

    passThrough.on('data', (chunk: Buffer) => chunks.push(chunk));
    passThrough.on('end', () => resolve(Buffer.concat(chunks)));
    passThrough.on('error', reject);

    const archive = archiver('zip', { zlib: { level: 6 } });
    archive.on('error', reject);
    archive.pipe(passThrough);

    // Add the entire project directory, preserving folder structure
    archive.directory(projectDir, false);

    archive.finalize();
  });
}

/**
 * In-process spec-to-code generator.
 * Writes all generated files under `outDir` which maps to `src/` inside the
 * scaffolded Next.js project. Route handlers land in `src/app/api/…`,
 * interfaces in `src/interfaces/`, etc.
 */
function generateFromSpec(
  spec: Record<string, unknown>,
  outDir: string,
  middlewarePayload?: GeneratePayload['middlewares'],
  servicesPayload?: GeneratePayload['services'],
) {
  const filesWritten: string[] = [];

  const schemas = ((spec.components as Record<string, unknown>)?.schemas ??
    {}) as Record<string, Record<string, unknown>>;

  const paths = (spec.paths ?? {}) as Record<string, Record<string, unknown>>;

  // 1. Generate TypeScript interfaces
  const interfacesDir = path.join(outDir, 'interfaces');
  fs.mkdirSync(interfacesDir, { recursive: true });
  const interfaceNames: string[] = [];

  for (const [name, schema] of Object.entries(schemas)) {
    interfaceNames.push(name);
    const content = buildInterface(name, schema);
    const filePath = path.join(interfacesDir, `${name}.ts`);
    fs.writeFileSync(filePath, content, 'utf-8');
    filesWritten.push(filePath);
  }

  // Barrel export for interfaces
  const ifBarrel = interfaceNames
    .map(n => `export type { ${n} } from './${n}';`)
    .join('\n');
  const ifBarrelPath = path.join(interfacesDir, 'index.ts');
  fs.writeFileSync(ifBarrelPath, `// Auto-generated\n${ifBarrel}\n`, 'utf-8');
  filesWritten.push(ifBarrelPath);

  // 2. Generate Zod schemas
  const validationDir = path.join(outDir, 'validation');
  fs.mkdirSync(validationDir, { recursive: true });
  const zodNames: string[] = [];

  for (const [name, schema] of Object.entries(schemas)) {
    zodNames.push(`${name}Schema`);
    const content = buildZodSchema(name, schema);
    const filePath = path.join(validationDir, `${name}Schema.ts`);
    fs.writeFileSync(filePath, content, 'utf-8');
    filesWritten.push(filePath);
  }

  const zodBarrel = zodNames
    .map(n => `export { ${n} } from './${n}';`)
    .join('\n');
  const zodBarrelPath = path.join(validationDir, 'index.ts');
  fs.writeFileSync(zodBarrelPath, `// Auto-generated\n${zodBarrel}\n`, 'utf-8');
  filesWritten.push(zodBarrelPath);

  // 3. Generate route handlers — placed under src/app/api/
  const routesDir = path.join(outDir, 'app', 'api');
  for (const [apiPath, methods] of Object.entries(paths)) {
    const folderPath = apiPath
      .split('/')
      .filter(Boolean)
      .map(seg =>
        seg.startsWith('{') && seg.endsWith('}')
          ? `[${seg.slice(1, -1)}]`
          : seg,
      )
      .join('/');

    const routeDir = path.join(routesDir, folderPath);
    fs.mkdirSync(routeDir, { recursive: true });

    const content = buildRouteFile(apiPath, methods);
    const routePath = path.join(routeDir, 'route.ts');
    fs.writeFileSync(routePath, content, 'utf-8');
    filesWritten.push(routePath);
  }

  // 4. Generate middleware utility helpers under src/lib/
  const libDir = path.join(outDir, 'lib');
  fs.mkdirSync(libDir, { recursive: true });
  fs.writeFileSync(
    path.join(libDir, 'middleware.ts'),
    MIDDLEWARE_TEMPLATE,
    'utf-8',
  );
  fs.writeFileSync(
    path.join(libDir, 'validation.ts'),
    VALIDATION_TEMPLATE,
    'utf-8',
  );
  fs.writeFileSync(
    path.join(libDir, 'baseService.ts'),
    BASE_SERVICE_TEMPLATE,
    'utf-8',
  );
  filesWritten.push(
    path.join(libDir, 'middleware.ts'),
    path.join(libDir, 'validation.ts'),
    path.join(libDir, 'baseService.ts'),
  );

  // 5. Generate middleware files under src/middlewares/
  const mwDir = path.join(outDir, 'middlewares');
  fs.mkdirSync(mwDir, { recursive: true });
  const allMiddlewareNames: string[] = [];

  // 5a. User-authored middleware code (from the UI)
  const enabledMws = (middlewarePayload ?? []).filter(m => m.enabled);
  for (const mw of enabledMws) {
    allMiddlewareNames.push(mw.name);
    fs.writeFileSync(path.join(mwDir, `${mw.name}.ts`), mw.code, 'utf-8');
    filesWritten.push(path.join(mwDir, `${mw.name}.ts`));
  }

  // 5b. Placeholders for spec-referenced middlewares not covered above
  const specMiddlewareNames = collectMiddlewareNames(paths);
  const coveredNames = new Set(allMiddlewareNames);
  for (const mwName of specMiddlewareNames) {
    if (!coveredNames.has(mwName)) {
      allMiddlewareNames.push(mwName);
      const mwContent = buildMiddlewarePlaceholder(mwName);
      fs.writeFileSync(path.join(mwDir, `${mwName}.ts`), mwContent, 'utf-8');
      filesWritten.push(path.join(mwDir, `${mwName}.ts`));
    }
  }

  // 5c. Barrel export
  if (allMiddlewareNames.length > 0) {
    const barrel = allMiddlewareNames
      .map(n => `export { ${n} } from './${n}';`)
      .join('\n');
    const barrelPath = path.join(mwDir, 'index.ts');
    fs.writeFileSync(barrelPath, `// Auto-generated\n${barrel}\n`, 'utf-8');
    filesWritten.push(barrelPath);
  }

  // 5d. Next.js middleware.ts entrypoint for global middlewares — lives at src/
  const globalMws = enabledMws.filter(m => m.scope === 'global');
  if (globalMws.length > 0) {
    const entrypoint = buildGlobalMiddlewareEntrypoint(globalMws);
    const entrypointPath = path.join(outDir, 'middleware.ts');
    fs.writeFileSync(entrypointPath, entrypoint, 'utf-8');
    filesWritten.push(entrypointPath);
  }

  // 6. Generate service class files under src/services/
  const allServices = servicesPayload ?? [];
  const namedServices = allServices.filter(s => s.name);

  if (namedServices.length > 0) {
    const servicesDir = path.join(outDir, 'services');
    fs.mkdirSync(servicesDir, { recursive: true });

    for (const svc of namedServices) {
      const fileContent = svc.code || buildDefaultServiceClass(svc.name);
      const fileName = `${svc.name}.ts`;
      fs.writeFileSync(path.join(servicesDir, fileName), fileContent, 'utf-8');
      filesWritten.push(path.join(servicesDir, fileName));
    }

    // Barrel export
    const barrel = namedServices
      .map(s => `export * from './${s.name}';`)
      .join('\n');
    fs.writeFileSync(
      path.join(servicesDir, 'index.ts'),
      `// Auto-generated\n${barrel}\n`,
      'utf-8',
    );
    filesWritten.push(path.join(servicesDir, 'index.ts'));
  }

  // 6b. Collect external service dependencies → package.json fragment
  const allDeps: Record<string, string> = {};
  for (const svc of namedServices) {
    for (const dep of svc.dependencies ?? []) {
      if (dep.name) allDeps[dep.name] = dep.version || 'latest';
    }
  }
  if (Object.keys(allDeps).length > 0) {
    const depsContent = JSON.stringify({ dependencies: allDeps }, null, 2);
    fs.writeFileSync(
      path.join(outDir, 'service-dependencies.json'),
      depsContent,
      'utf-8',
    );
    filesWritten.push(path.join(outDir, 'service-dependencies.json'));
  }

  // 6c. Aggregated health route for all named services
  if (namedServices.length > 0) {
    const healthRoute = buildAggregatedHealthRoute(
      namedServices.map(s => s.name),
    );
    const healthRouteDir = path.join(outDir, 'app', 'api', 'health');
    fs.mkdirSync(healthRouteDir, { recursive: true });
    fs.writeFileSync(
      path.join(healthRouteDir, 'route.ts'),
      healthRoute,
      'utf-8',
    );
    filesWritten.push(path.join(healthRouteDir, 'route.ts'));
  }

  return {
    filesWritten,
    interfacesCount: interfaceNames.length,
    schemasCount: zodNames.length,
    routesCount: Object.keys(paths).length,
  };
}

