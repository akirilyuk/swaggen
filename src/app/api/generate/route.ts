interface GeneratePayload {
  spec: Record<string, unknown>;
  projectName: string;
  middlewares?: {
    name: string;
    code: string;
    enabled: boolean;
    scope: 'global' | 'route';
  }[];
  services?: {
    name: string;
    code?: string;
    dependencies?: { name: string; version: string }[];
    healthCheck?: {
      enabled: boolean;
      path: string;
      code: string;
    };
  }[];
  dataStorage?: {
    provider: string;
    connectionString: string;
    enabled: boolean;
  };
  /** Frontend pages (Swaggen canvas + legacy) for `/site` static export */
  pages?: UIPage[];
  /** Full project JSON for reference / re-import (optional) */
  projectSnapshot?: unknown;
}

interface GeneratedFile {
  path: string;
  content: string;
}

