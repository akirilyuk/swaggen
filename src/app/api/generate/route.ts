function buildInterface(name: string, schema: Record<string, unknown>): string {
  const props = (schema.properties ?? {}) as Record<
    string,
    Record<string, unknown>
  >;
  const required = new Set((schema.required ?? []) as string[]);
  const lines = [
    `// Auto-generated from OpenAPI spec`,
    `export interface ${name} {`,
  ];

  for (const [propName, propSchema] of Object.entries(props)) {
    const tsType = jsonToTsType(propSchema);
    const opt = required.has(propName) ? '' : '?';
    if (propSchema.description)
      lines.push(`  /** ${propSchema.description} */`);
    lines.push(`  ${propName}${opt}: ${tsType};`);
  }

  lines.push('}', '');
  return lines.join('\n');
}

function jsonToTsType(schema: Record<string, unknown>): string {
  if (schema.$ref) {
    const parts = (schema.$ref as string).split('/');
    return parts[parts.length - 1];
  }
  if (schema.type === 'array') {
    const items = schema.items as Record<string, unknown> | undefined;
    return items ? `${jsonToTsType(items)}[]` : 'unknown[]';
  }
  if (schema.enum) {
    return (schema.enum as string[]).map(v => `'${v}'`).join(' | ');
  }

  const typeMap: Record<string, string> = {
    string: 'string',
    number: 'number',
    integer: 'number',
    boolean: 'boolean',
    object: 'Record<string, unknown>',
  };
  return typeMap[(schema.type as string) ?? 'string'] ?? 'unknown';
}

function buildZodSchema(name: string, schema: Record<string, unknown>): string {
  const props = (schema.properties ?? {}) as Record<
    string,
    Record<string, unknown>
  >;
  const required = new Set((schema.required ?? []) as string[]);

  const fields = Object.entries(props)
    .map(([propName, propSchema]) => {
      const zodType = jsonToZodType(propSchema);
      const opt = required.has(propName) ? '' : '.optional()';
      return `  ${propName}: ${zodType}${opt},`;
    })
    .join('\n');

  return [
    `// Auto-generated from OpenAPI spec`,
    `import { z } from 'zod';`,
    '',
    `export const ${name}Schema = z.object({`,
    fields,
    `});`,
    '',
    `export type ${name} = z.infer<typeof ${name}Schema>;`,
    '',
  ].join('\n');
}

function jsonToZodType(schema: Record<string, unknown>): string {
  if (schema.$ref) {
    const parts = (schema.$ref as string).split('/');
    return `${parts[parts.length - 1]}Schema`;
  }
  if (schema.type === 'array') {
    const items = schema.items as Record<string, unknown> | undefined;
    return items ? `z.array(${jsonToZodType(items)})` : 'z.array(z.unknown())';
  }
  if (schema.enum) {
    const values = (schema.enum as string[]).map(v => `'${v}'`).join(', ');
    return `z.enum([${values}])`;
  }

  const typeMap: Record<string, string> = {
    string: 'z.string()',
    number: 'z.number()',
    integer: 'z.number().int()',
    boolean: 'z.boolean()',
    object: 'z.record(z.unknown())',
  };
  const base = typeMap[(schema.type as string) ?? 'string'] ?? 'z.unknown()';

  if (schema.format === 'uuid') return 'z.string().uuid()';
  if (schema.format === 'date-time') return 'z.string().datetime()';
  if (schema.format === 'email') return 'z.string().email()';

  return base;
}

function buildRouteFile(
  apiPath: string,
  methods: Record<string, unknown>,
): string {
  const lines = [
    `// Auto-generated route handler for ${apiPath}`,
    `import { NextRequest, NextResponse } from 'next/server';`,
    '',
  ];

  const httpMethods = ['get', 'post', 'put', 'patch', 'delete'];
  for (const method of httpMethods) {
    const op = methods[method] as Record<string, unknown> | undefined;
    if (!op) continue;

    const fnName = method.toUpperCase();
    const operationId = (op.operationId as string) ?? `${method}Handler`;

    lines.push(`/** ${op.summary ?? operationId} */`);
    lines.push(`export async function ${fnName}(req: NextRequest) {`);
    lines.push(`  // TODO: implement ${operationId}`);
    lines.push(
      `  return NextResponse.json({ message: '${operationId} not implemented' }, { status: 501 });`,
    );
    lines.push(`}`);
    lines.push('');
  }

  return lines.join('\n');
}

function collectMiddlewareNames(
  paths: Record<string, Record<string, unknown>>,
): string[] {
  const names = new Set<string>();
  for (const methods of Object.values(paths)) {
    for (const op of Object.values(methods)) {
      const opObj = op as Record<string, unknown>;
      const mws = opObj['x-middlewares'] as string[] | undefined;
      if (mws) mws.forEach(m => names.add(m));
    }
  }
  return Array.from(names);
}

function buildMiddlewarePlaceholder(name: string): string {
  return [
    `// Auto-generated middleware placeholder: ${name}`,
    `import { NextRequest, NextResponse } from 'next/server';`,
    `import type { PipelineContext, SwaggenMiddlewareResult } from '../lib/middleware';`,
    '',
    `export async function ${name}(`,
    `  req: NextRequest,`,
    `  ctx: PipelineContext,`,
    `): Promise<SwaggenMiddlewareResult> {`,
    `  // TODO: implement ${name} middleware logic`,
    `  return {};`,
    `}`,
    '',
  ].join('\n');
}

function buildGlobalMiddlewareEntrypoint(
  globalMws: { name: string; scope: string }[],
): string {
  const imports = globalMws
    .map(mw => `import { ${mw.name} } from './middlewares/${mw.name}';`)
    .join('\n');

  const chain = globalMws.map(mw => `  ${mw.name},`).join('\n');

  return `// Auto-generated Next.js middleware entrypoint
import { NextRequest, NextResponse } from 'next/server';

${imports}
import { createPipelineContext, mergeContext } from './lib/middleware';

const globalMiddlewares = [
${chain}
];

export async function middleware(req: NextRequest) {
  const ctx = createPipelineContext();

  for (const mw of globalMiddlewares) {
    try {
      const result = await mw(req, ctx);
      if (result.response) return result.response;
      mergeContext(ctx, result.ctx);
    } catch (error) {
      console.error(\`[middleware] \${mw.name} failed:\`, error);
      return NextResponse.json(
        { error: 'Internal middleware error' },
        { status: 500 },
      );
    }
  }

  const response = NextResponse.next();

  if (ctx.corsHeaders) {
    for (const [key, value] of Object.entries(ctx.corsHeaders)) {
      response.headers.set(key, value);
    }
  }
  if (ctx.requestId) {
    response.headers.set('x-request-id', ctx.requestId);
  }

  return response;
}

export const config = {
  matcher: ['/api/:path*'],
};
`;
}

function buildDefaultServiceClass(name: string): string {
  const varName = name[0].toLowerCase() + name.slice(1);
  return `import { BaseService, type HealthCheckResult } from '../lib/baseService';

export class ${name} extends BaseService {
  readonly serviceName = '${name}';

  async healthCheck(): Promise<HealthCheckResult> {
    return this.health(async () => {
      // Add dependency checks here (DB, cache, third-party APIs, etc.)
      return this.ok({ ready: true });
    });
  }
}

export const ${varName} = new ${name}();
`;
}

function buildAggregatedHealthRoute(serviceNames: string[]): string {
  const imports = serviceNames
    .map(name => {
      const varName = name[0].toLowerCase() + name.slice(1);
      return `import { ${varName} } from '../../../services/${name}';`;
    })
    .join('\n');

  const instances = serviceNames
    .map(name => `  ${name[0].toLowerCase()}${name.slice(1)}`)
    .join(',\n');

  return `// Auto-generated aggregated health endpoint
import { NextResponse } from 'next/server';

${imports}

const services = [
${instances},
];

export async function GET() {
  const results = await Promise.allSettled(
    services.map(svc => svc.healthCheck()),
  );

  const checks = results.map((r, i) => {
    if (r.status === 'fulfilled') return r.value;
    return {
      service: services[i].serviceName,
      healthy: false,
      details: { error: (r.reason as Error)?.message ?? 'Unknown error' },
    };
  });

  const allHealthy = checks.every(c => c.healthy);

  return NextResponse.json(
    { healthy: allHealthy, services: checks },
    { status: allHealthy ? 200 : 503 },
  );
}
`;
}

