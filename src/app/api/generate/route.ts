export async function POST(req: NextRequest) {
  let tmpDir: string | null = null;

  try {
    const payload: GeneratePayload = await req.json();

    if (!payload.spec) {
      return NextResponse.json(
        { error: 'Missing "spec" in request body' },
        { status: 400 },
      );
    }

    const projectSlug = (payload.projectName || 'my-app')
      .toLowerCase()
      .replace(/\s+/g, '-');

    // 1. Create temp directory
    tmpDir = fs.mkdtempSync(path.join(os.tmpdir(), 'swaggen-'));

    // 2. Scaffold a full Next.js project structure at the root
    const projectDir = path.join(tmpDir, projectSlug);
    fs.mkdirSync(projectDir, { recursive: true });
    scaffoldNextProject(projectDir, payload.projectName || 'My App');

    // 3. Overlay generated files into src/ so they sit inside the project
    const srcDir = path.join(projectDir, 'src');
    fs.mkdirSync(srcDir, { recursive: true });

    const result = generateFromSpec(
      payload.spec,
      srcDir,
      payload.middlewares,
      payload.services,
    );

    writeSiteExportToProject(projectDir, {
      pages: payload.pages ?? [],
      projectSnapshot: payload.projectSnapshot,
      projectName: payload.projectName || 'My App',
    });

    // 4. Read generated file list for the response metadata (before zipping)
    const generatedFiles = readDirRecursive(srcDir, projectDir);

    // 5. Build ZIP archive of the entire project directory
    const zipBuffer = await buildZipBuffer(projectDir);

    return new NextResponse(new Uint8Array(zipBuffer), {
      status: 200,
      headers: {
        'Content-Type': 'application/zip',
        'Content-Disposition': `attachment; filename="${projectSlug}.zip"`,
        'X-Files-Count': String(generatedFiles.length),
        'X-Interfaces-Count': String(result.interfacesCount),
        'X-Schemas-Count': String(result.schemasCount),
        'X-Routes-Count': String(result.routesCount),
      },
    });
  } catch (e) {
    console.error('Generation failed:', e);
    return NextResponse.json({ error: (e as Error).message }, { status: 500 });
  } finally {
    if (tmpDir) cleanup(tmpDir);
  }
}
